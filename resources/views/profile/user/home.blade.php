<script>
$(document).ready(function(){
   // Define a function to load content and update the URL
   function loadContentAndChangeURL(url) {
      $('.content').load(url);
      history.pushState(null, null, url);
   }

   // Handle navigation clicks
   $('#nav-explore').click(function(){
      loadContentAndChangeURL('explore');
   });

   $('#nav-shelf').click(function(){
      loadContentAndChangeURL('shelf');
   });

   $('#nav-inbox').click(function(){
      loadContentAndChangeURL('inbox');
   });

   $('#nav-me').click(function(){
      loadContentAndChangeURL('me');
   });

   // Handle browser back/forward buttons
   window.onpopstate = function(event) {
      if (event.state) {
         $('.content').load(document.location.href);
      }
   };
});

